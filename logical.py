#and operator
# example 1
x = 5
print(x > 3 and x < 10)#True because both the conditions are true

# example 2 
y = 12
print(y > 10 and y % 5 == 0) # False because the second condition is false 

# or operator
x = 5
print(x > 3 or x < 10)#True because both the conditions are true

# example 2 
y = 12
print(y > 10 or y % 5 == 0) # False because the second condition is false 

#not operator
x = 5
print(not(x > 3 and x < 10))#True because both the conditions are true

# example 2 
y = 12
print(not(y > 10 and y % 5 == 0))# False because the second condition is false 
